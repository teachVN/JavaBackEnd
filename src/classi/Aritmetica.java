package classi;

public class Aritmetica {

    public double somma(int a, int b){
        return a+b;
    }

    public double somma(int c, short d){
        return c+d;
    }

    public double somma(short a, int b){
        return a+b;
    }

    public double somma(int a, int b, int c){
        return a+b+c;
    }

    public double somma(int a, short b, int c){
        return a+b+c;
    }
}
